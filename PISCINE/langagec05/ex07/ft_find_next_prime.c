int	ft_find_next_prime(int nb)
{
	int	i;
	int	resultat;

	i = 2;
	resultat = 0;
	if (nb <= 1)
		return (2);
	while (i < 47000 && i < nb)
	{
		if (nb % i == 0)
		{
			nb++;
			i = 1;
		}
		i++;
	}	
	return (nb);
}
